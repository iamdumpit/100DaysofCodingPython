def bet_result(player_bet,cpu_bet):
    global player_score, cpu_score
    # Display input, Rock = 0, Paper = 1, Scissors = 2
    # if Player is Rock and CPU is Paper, Player loses
    if player_bet == 0 and cpu_bet == 2:
        print(f'{Fore.LIGHTGREEN_EX}You win!\n{Style.DIM}')
        player_score += 1
    # if Player is Rock and CPU is Scissors, Player wins
    elif cpu_bet == 0 and player_bet == 2:
        print(f'{Fore.LIGHTGREEN_EX}You lose!\n{Style.DIM}')
        cpu_score += 1
    # if both picked Rock, Draw
    elif player_bet > cpu_bet:
        print(f'{Fore.LIGHTGREEN_EX}You win!\n{Style.DIM}')
        player_score += 1
    elif cpu_bet > player_bet:
        print(f'{Fore.LIGHTGREEN_EX}You lose!\n{Style.DIM}')
        cpu_score += 1
    elif player_bet == cpu_bet:
        print(f'{Fore.LIGHTGREEN_EX}DRAW!\n{Style.DIM}')
    show_score(player_score,cpu_score)
def main():

    # Initialize try again
    try_again = "Y"

    print(f'{Fore.LIGHTGREEN_EX}{hello_text}{Style.DIM}')
    while try_again == "Y":
        try:
            player_bet = int(input("What do you choose? Type 0 for Rock, 1 for Paper or 2 for Scissors. \n"))
            cpu_bet = random.randint(0, 2)

            if player_bet >= 3 or player_bet < 0:
                print("Wrong option! Type only 0 for Rock, 1 for Paper or 2 for Scissors.")
            else:
                bet_style(player_bet,cpu_bet)
                bet_result(player_bet,cpu_bet)
        except:
            pass
        try_again = input("Try again (Y/N)?\n").upper()
        clear()
    print(f'{Fore.LIGHTGREEN_EX}{thank_you}{Style.DIM}')
    return 0


