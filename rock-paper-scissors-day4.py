# define our clear function
def clear():
    # for windows
    if name == 'nt':
        _ = system('cls')

    # for mac and linux(here, os.name is 'posix')
    else:
        _ = system('clear')

def show_score(a, b):
    print("===== Scoreboard =====")
    print("======================")
    print(f"Player: {a}\nCPU: {b}")
    print("======================")

